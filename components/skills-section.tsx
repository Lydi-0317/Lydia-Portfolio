function SkillCard({
  skill,
  categoryIndex,
  skillIndex,
}: {
  skill: Skill;
  categoryIndex: number;
  skillIndex: number;
}) {
  const [hovered, setHovered] = useState(false);
  const isPrimary = skill?.primary === true;

  if (!skill?.name) return null;

  return (
    <motion.div
      initial={{ opacity: 0, scale: 0.8 }}
      whileInView={{ opacity: 1, scale: 1 }}
      viewport={{ once: true }}
      transition={{
        duration: 0.3,
        delay: 0.1 * categoryIndex + 0.04 * skillIndex,
      }}
      onMouseEnter={() => setHovered(true)}
      onMouseLeave={() => setHovered(false)}
      className={`group relative flex flex-col items-center justify-center rounded-xl px-3 ${
        isPrimary
          ? "col-span-2 bg-primary/10 py-8 ring-1 ring-primary/20"
          : "bg-secondary/50 py-5"
      }`}
    >
      {/* Neon glow background */}
      <motion.div
        className="pointer-events-none absolute inset-0 rounded-xl"
        animate={{
          boxShadow: hovered
            ? `0 0 20px ${skill.color}35, 0 0 40px ${skill.color}18, inset 0 0 20px ${skill.color}08`
            : "0 0 0px transparent",
        }}
        transition={{ duration: 0.3 }}
      />

      {/* Neon border */}
      <motion.div
        className="pointer-events-none absolute inset-0 rounded-xl"
        animate={{
          border: hovered
            ? `1px solid ${skill.color}50`
            : `1px solid ${isPrimary ? `${skill.color}20` : "transparent"}`,
        }}
        transition={{ duration: 0.3 }}
      />

      {/* Floating icon */}
      <motion.div
        animate={{ y: hovered ? -6 : 0 }}
        transition={{ type: "spring", stiffness: 300, damping: 15 }}
        className={`relative flex items-center justify-center rounded-lg ${
          isPrimary ? "h-16 w-16" : "h-12 w-12"
        }`}
        style={{
          backgroundColor: hovered ? `${skill.color}20` : `${skill.color}10`,
        }}
      >
        <motion.div
          animate={{
            filter: hovered
              ? `drop-shadow(0 0 8px ${skill.color}80) drop-shadow(0 0 16px ${skill.color}40)`
              : "drop-shadow(0 0 0px transparent)",
          }}
          transition={{ duration: 0.3 }}
        >
          <svg
            width={isPrimary ? 32 : 24}
            height={isPrimary ? 32 : 24}
            viewBox="0 0 24 24"
            fill={skill.color}
            xmlns="http://www.w3.org/2000/svg"
            role="img"
            aria-label={skill.name}
          >
            <path d={skill.svgPath} />
          </svg>
        </motion.div>
      </motion.div>

      {/* Skill name */}
      <motion.span
        animate={{
          y: hovered ? 2 : 0,
          color: hovered ? skill.color : undefined,
        }}
        transition={{ duration: 0.2 }}
        className={`mt-2.5 text-center font-medium text-muted-foreground ${
          isPrimary ? "text-sm" : "text-xs"
        }`}
      >
        {skill.name}
        {isPrimary && (
          <span className="ml-1.5 inline-block rounded-full bg-primary/20 px-2 py-0.5 text-[10px] text-primary">
            Primary
          </span>
        )}
      </motion.span>

      {/* Tooltip on hover */}
      <AnimatePresence>
        {hovered && (
          <motion.div
            initial={{ opacity: 0, y: 8, scale: 0.9 }}
            animate={{ opacity: 1, y: 0, scale: 1 }}
            exit={{ opacity: 0, y: 4, scale: 0.95 }}
            transition={{ duration: 0.15, ease: "easeOut" }}
            className="pointer-events-none absolute -bottom-10 z-10 whitespace-nowrap rounded-md px-2.5 py-1 text-[11px] font-medium text-foreground"
            style={{
              backgroundColor: `${skill.color}20`,
              border: `1px solid ${skill.color}30`,
              boxShadow: `0 4px 12px ${skill.color}15`,
            }}
          >
            {skill.name}
            <div
              className="absolute -top-1 left-1/2 h-2 w-2 -translate-x-1/2 rotate-45"
              style={{
                backgroundColor: `${skill.color}20`,
                borderLeft: `1px solid ${skill.color}30`,
                borderTop: `1px solid ${skill.color}30`,
              }}
            />
          </motion.div>
        )}
      </AnimatePresence>
    </motion.div>
  );
}

